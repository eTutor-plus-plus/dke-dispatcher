package at.jku.dke.etutor.modules.nf.ui;

public class SpecificationParserException extends Exception {

	public SpecificationParserException() {
		super();
	}

	public SpecificationParserException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SpecificationParserException(String arg0) {
		super(arg0);
	}

	public SpecificationParserException(Throwable arg0) {
		super(arg0);
	}
}

